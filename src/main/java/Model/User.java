package Model;

public class User {

	private int accno;
	private String name;
	
	private int amount;
	private String phoneNo;
	public int getAccno() {
		return accno;
	}
	public void setAccno(int accno) {
		this.accno = accno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public String getPhoneNo() {
		return phoneNo;
	}
	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}
	@Override
	public String toString() {
		return "User [accno=" + accno + ", name=" + name + ", amount=" + amount + ", phoneNo=" + phoneNo + "]";
	}
	
}
